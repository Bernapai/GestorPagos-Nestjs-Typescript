import { Entity, PrimaryGeneratedColumn, Column } from 'typeorm';

@Entity('users') // nombre de la tabla
export class User {
  @PrimaryGeneratedColumn()
  id: number;

  @Column()
  name: string;

  @Column({ unique: true })
  email: string;

  @Column()
  password: string;

  @Column({ default: true })
  isActive: boolean;
}
